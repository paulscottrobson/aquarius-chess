; ***************************************************************************************
;
;									Compile HL to Code Space
;
; ***************************************************************************************

CompileWord:
	ex 		de,hl
	push 	hl
	ld 		hl,(CodeNextFree)
	ld 		(hl),e
	inc 	hl
	ld 		(hl),d
	inc 	hl
	ld 		(CodeNextFree),hl
	pop 	hl
	ex 		de,hl
	ret

; ***************************************************************************************
;
;									Compile A to Code Space
;
; ***************************************************************************************

CompileByte:
	push 	hl
	ld 		hl,(CodeNextFree)
	ld 		(hl),a
	inc 	hl
	ld 		(CodeNextFree),hl
	pop 	hl
	ret
